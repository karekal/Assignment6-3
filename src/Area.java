
public class Area implements Shapes{
	public void areaOfSquare(int x){
		System.out.println("area of a square:"+(x*x));
}
    public void areaOfTriangle(int c,int d){
    	System.out.println("area of a triangle"+(0.5*c*d));
    }
    public void areaOfRectangle(int a,int b){
    	System.out.println("area of rectangle"+(a*b));
    }
}
